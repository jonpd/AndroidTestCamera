package net.vmci.oc.testswapcamera;

public class CameraResolution {

    private int width;
    private int height;

    public CameraResolution(int w, int h) {

        width = w;
        height = h;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
